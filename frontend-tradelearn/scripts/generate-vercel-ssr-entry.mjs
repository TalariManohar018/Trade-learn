import { readFile, writeFile } from "node:fs/promises";
import { join } from "node:path";

const manifestPath = join(process.cwd(), "dist", "server", ".vite", "manifest.json");
const outPath = join(process.cwd(), "dist", "server", "vercel-ssr-entry.mjs");

const manifestRaw = await readFile(manifestPath, "utf8");
const manifest = JSON.parse(manifestRaw);

const serverEntry = Object.values(manifest).find(
  (entry) => entry && typeof entry === "object" && entry.name === "server" && typeof entry.file === "string",
);

if (!serverEntry) {
  throw new Error(`Could not find SSR server entry in ${manifestPath}`);
}

const serverFile = serverEntry.file;
if (!serverFile.startsWith("assets/")) {
  throw new Error(`Unexpected server entry file: ${serverFile}`);
}

const serverBundlePath = join(process.cwd(), "dist", "server", serverFile);
const serverBundle = await readFile(serverBundlePath, "utf8");

// Vite bundles the SSR entry as a namespace export with a minified alias (e.g. `server as a1`).
// We detect that alias and re-export `server.default` as the default export.
const aliasMatch = serverBundle.match(/\bserver\s+as\s+([\w$]+)/);
if (!aliasMatch) {
  throw new Error(`Could not detect exported server alias in ${serverBundlePath}`);
}

const serverAlias = aliasMatch[1];
const contents = `// Auto-generated by scripts/generate-vercel-ssr-entry.mjs\nimport { ${serverAlias} as serverModule } from "./${serverFile}";\nexport default (serverModule?.default ?? serverModule);\n`;
await writeFile(outPath, contents, "utf8");

console.log(`[vercel] wrote SSR entry: dist/server/vercel-ssr-entry.mjs -> ${serverFile}`);
